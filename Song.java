public class Song {
    int sid;
    String name;
    int time;
    int heart;
    int road;
    int bliss;
    public Song(int sid, String name, int time, int heart, int road, int bliss){
        this.sid=sid;
        this.name=name;
        this.time=time;
        this.heart=heart;
        this.road=road;
        this.bliss=bliss;
    }
}
